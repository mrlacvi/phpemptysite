<p style="font-size: 16px; " align="center"> Intel apresenta a quarta geração dos processadores Core™ i7 Extreme
</p>
<p align="center"><img src="intel.jpg" width="730" height="318" /></p>


<p align="justify"> Para a maioria das pessoas, um PC com processador Intel Core i5 ou AMD A6, ambos superam as necessidades diárias mas há 1% de usuários Hard que exige o máximo de desempenho para jogos e computação high-end. Pensando neste nicho Hard, a Intel desenvolveu o novo "Extreme Edition", Hexa-core i7-4960X, para os membros dessa elite. Os novos processadores estão no topo da lista de preços da Intel, podendo custar até 990 dólares somente o processador, não incluindo o cooler.</p>

<p align="justify">Apesar de pertencer a já antiga série 4000, os novos chips Core i7 são baseados na microarquitetura Ivy Bridge da Intel, e não em Haswell. Isso significa que ele está restrito a placas-mãe com soquete 2011 e chipset Intel X79. A boa notícia para as pessoas que procuram atualizar seu  Extreme anterior é que os novos chips são compatíveis com a Placa Mãe, mas você vai precisar atualizar a BIOS  com um novo firmware. Essa opção irá lhe poupar algumas centenas de dólares, mas os fabricantes de placas-mãe irão forçar a venda de novos modelos.</p>

<p align="justify">O clock base deste processador é de 3,6 GHz, mas esse número pode chegar a 4 GHz através do recurso Turbo Boost. A CPU possui seis núcleos físicos, mas, com a tecnologia Hyper Threading da Intel, ela pode realizar até 12 processos simultaneamente, já que o sistema operacional enxerga 12 núcleos lógicos. O processador também possui um cache L3 de 15 MB e suporta memória DDR3 1.866 MHz em modo quad channel. De acordo com a Intel, o novo Core i7-4960X apresentará um aumento de desempenho de cerca de 7% em jogos 3D, baseado no teste 3DMark do antigo Core i7-3960X Extreme Edition. A empresa ainda afirma que o novo Core i7-4960X é 36% mais rápido com jogos em 3D em comparação à arquitetura Haswell.</p>
<p><img src="intel2.jpg" width="730" height="471" /></p> 
<p align="justify"> Uma opção de Placa Mãe a ser utilizada é a Asus Rampage IV Extreme que possui slots PCIe para acomodar várias placas gráficas. O chipset X79 de estrutura LGA 2011 suporta PCIe 3.0, o suficiente para suportar várias placas gráficas high-end (usando a tecnologia CrossFire X da AMD ou SLI da Nvidia). Essa capacidade combinada com uma CPU high-end e muita memória, compõem um sistema "soberbo" de desempenho.</p>
<p style="font-size: 14px; font-weight: bold;" align="center">Desvenda o significado das letras depois do número dos processadores</p>
<p align="justify">Para facilitar a identificação dos modelos, a Intel apresenta uma letra após o número de modelo dos processadores. Veja o que cada sufixo significa:

X: a letra X depois do código do processador significa que aquele modelo pertence à linha Extreme Edition, ou seja, os modelos mais poderosos. Todos possuem o clock destravado;
K: o sufixo K significa que os processadores são top de linha, mas não os principais. Além disso, a letra também indica que o processador tem o clock destravado.
Quando a CPU não traz nenhum sufixo ao lado do número, significa que é um modelo tradicional de processador, sem nenhuma característica especial. Todos esses modelos possuem o clock travado de fábrica.</p>
<p><img src="intel3.png" width="730" height="378" /></p>
<p align="justify">Se os entusiastas HI-TEC não se sentirem motivados a um upgrade, existe uma categoria que deve amar essa nova arquitetura:o segmento de servidores e estações de trabalho. Apesar de o desempenho não ser gritante em relação à geração anterior, o Ivy Bridge-E é incrivelmente mais eficiente que o seu antecessor, principalmente no que diz respeito ao consumo energético e geração de calor. uma máquina montada com um processador Intel Core i7-4960X utilizando quatro canais de memória não deixa de ser impressionante e rápida. Apesar do chipset X79 ser um pouco datado, ele é perfeitamente capaz de fornecer o suporte que a nova arquitetura precisa.</p>

<p align="justify">Se você já possui uma máquina com um processador I7-3960X, não existem muitos motivos para trocar de processador por enquanto. Caso pretenda montar um computador absurdamente poderoso, o novo Core i7-4960X é a melhor opção.</div>
